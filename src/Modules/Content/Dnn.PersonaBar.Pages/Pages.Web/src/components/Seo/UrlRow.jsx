import React, {Component, PropTypes} from "react";
import GridCell from "dnn-grid-cell";
import { EditIcon, TrashIcon } from "dnn-svg-icons";
import styles from "./style.less";

class UrlRow extends Component {
    render() {
        const {url, onEdit, onDelete} = this.props;

        return (
            /* eslint-disable react/no-danger */
            <div className={styles.UrlRow} >
                <GridCell columnSize={50} >
                    {url.url}
                </GridCell>
                <GridCell  columnSize={20} >
                    {url.type}
                </GridCell>
                <GridCell  columnSize={20} >
                    {url.generatedBy}
                </GridCell>
                {!url.autoGenerated &&
                    <GridCell  columnSize={10} >
                        <div className="extension-action" onClick={onEdit} dangerouslySetInnerHTML={{ __html: TrashIcon }}></div>
                        <div className="extension-action" onClick={onDelete} dangerouslySetInnerHTML={{ __html: EditIcon }}></div>
                    </GridCell>}
            </div>
            /* eslint-enable react/no-danger */
        );
    }
}

UrlRow.propTypes = {
    url: PropTypes.object,
    onEdit: PropTypes.func,
    onDelete: PropTypes.func
};

export default UrlRow;