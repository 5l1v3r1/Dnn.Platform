import React, {Component, PropTypes} from "react";
import GridCell from "dnn-grid-cell";
import { EditIcon, TrashIcon } from "dnn-svg-icons";
import styles from "./style.less";
import Localization from "../../../localization";
import EditUrl from "./EditUrl";

class UrlRow extends Component {
    onSave(url) {
        
    }
    
    onCancel() {
        
    }
    
    render() {
        const {url, siteAliases, primaryAliasId, isEditingModule, onChange, onEdit, onDelete} = this.props;
        const editClassName = "extension-action" + (isEditingModule ? " selected" : "");
        let generatedBy = Localization.get("Pages_Seo_GeneratedByAutomatic");
        
        if (!url.autoGenerated) {
            generatedBy = "yo mismo";
        }
        
        return (
            /* eslint-disable react/no-danger */
            <div className={styles.urlRow} >
                <GridCell columnSize={50} >
                    {url.path}
                </GridCell>
                <GridCell  columnSize={20} >
                    {url.statusCode.Value}
                </GridCell>
                <GridCell  columnSize={20} >
                    {generatedBy}
                </GridCell>
                {!url.autoGenerated &&
                    <GridCell  columnSize={10}>
                        <div className="extension-action" onClick={onEdit.bind(this, url)} dangerouslySetInnerHTML={{ __html: TrashIcon }}></div>
                        <div className="extension-action" onClick={onDelete.bind(this, url)} dangerouslySetInnerHTML={{ __html: EditIcon }}></div>
                    </GridCell>
                }
            </div>
            /* eslint-enable react/no-danger */
        );
    }
}

UrlRow.propTypes = {
    url: PropTypes.object.isRequired,
    siteAliases: PropTypes.arrayOf(PropTypes.object).isRequired,
    primaryAliasId: PropTypes.number,
    isEditingModule: PropTypes.bool,
    onEdit: PropTypes.func.isRequired,
    onChange: PropTypes.func.isRequired,
    onDelete: PropTypes.func.isRequired
};

export default UrlRow;